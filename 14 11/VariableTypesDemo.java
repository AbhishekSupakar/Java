class VariableTypesDemo {
    public static void main(String[] args) {
		int i=10;
		char c='A';
		double d=99.92;
		boolean b=true;
		System.out.println("Integer value: " + i);
        System.out.println("Double value: " + d);
        System.out.println("Character value: " + c);
        System.out.println("Boolean value: " + b);
	}
}